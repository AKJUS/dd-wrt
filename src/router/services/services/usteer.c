#ifdef HAVE_USTEER
#include <stdlib.h>
#include <bcmnvram.h>
#include <shutils.h>
#include <utils.h>
#include <syslog.h>
#include <signal.h>
#include <services.h>

void start_usteer(void)
{
	// Only start if enabled
	if (!nvram_invmatchi("usteer_enable", 0))
		return;

	log_eval("usteer","-i", "br0", "-s", "&");
	cprintf("done\n");
	return;
}

void stop_usteer(void)
{
	stop_process("usteer", "daemon");
	return;
}
#endif